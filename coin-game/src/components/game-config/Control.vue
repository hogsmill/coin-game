<template>
  <div class="card bg-light mb-3 col-md-3 no-padding-r-l" v-if="!stateSet">
    <div class="card-body">
      <h5 class="card-title">Control</h5>
      <form>
        <div class="form-group">
          <label for="interval">Interval Between Coin Plays (ms)</label>
          <input
            type="text"
            class="form-control"
            id="interval"
            name="interval"
            v-model.lazy="interval"
          />
        </div>

        <div class="form-group">
          <label for="timeLimit">Time Limit Per Round (ms)</label>
          <input
            type="text"
            class="form-control"
            id="timeLimit"
            name="timeLimit"
            v-model.lazy="gameState['timeLimit']"
            v-on:change="updateGameState()"
          />
        </div>

        <div class="form-group">
          <label for="valueTimeLimit">Value First Round Time Limit (ms)</label>
          <input
            type="text"
            class="form-control"
            id="valueTimeLimit"
            name="valueTimeLimit"
            v-model.lazy="gameState['valueTimeLimit']"
            v-on:change="updateGameState()"
          />
        </div>
      </form>
    </div>
  </div>
</template>

<script>
import io from "socket.io-client";

export default {
  methods: {
    updateGameState() {
      this.socket.emit("updateGameState", this.$store.getters.getGameState)
    }
  },
  computed: {
    stateSet() {
      return this.$store.getters.getStateSet;
    },
    interval: {
      get() {
        return this.$store.getters.getInterval;
      },
      set(value) {
        this.socket.emit("updateInterval", value)
        //this.$store.dispatch("updateInterval", value);
      },
    },
    gameState() {
      return this.$store.getters.getGameState;
    }
  },
  created() {
    var host = "77.68.122.69"
    if (location.hostname == 'localhost') {
      host = 'localhost'
    }
    var connStr = "http://" + host + ":3000"
    console.log("Connecting to: " + connStr)
    this.socket = io(connStr)
  },
  mounted() {
    this.socket.on("updateInterval", (data) => {
      this.$store.dispatch("updateInterval", data);
    }),
    this.socket.on("updateGameState", (data) => {
      this.$store.dispatch("updateGameState", data);
    })
  }
};
</script>
