<template>
  <div class="config card bg-light mb-3 no-padding-r-l">
    <div class="card-body">
      <div class="control-header">
        <h5 class="card-title">
          Denominations and Currency
        </h5>
        <i v-if="showDenominations" @click="setShowDenominations(false)" title="collapse" class="fas fa-caret-up toggle" />
        <i v-if="!showDenominations" @click="setShowDenominations(true)" title="expand" class="fas fa-caret-down toggle" />
      </div>
      <div v-if="showDenominations">
        <table class="config-table">
          <tr>
            <td>
              Currency:
            </td>
            <td>
              <select class="form-control" id="currency">
                <option>-- Select --</option>
                <option v-for="(currency, index) in editingWorkshop.currencies" :key="index" :selected="currency.name == editingWorkshop.currency.name">
                  {{ currency.name }}
                </option>
              </select>
              <i class="fas fa-save" title="Save Changes" @click="updateCurrency()" />
            </td>
          </tr>
          <tr>
            <td colspan="2">
              <table class="denominations-table">
                <Denomination :socket="socket" :id="'twoPound'" :amount="200" />
                <Denomination :socket="socket" :id="'onePound'" :amount="100" />
                <Denomination :socket="socket" :id="'fifty'" :amount="50" />
                <Denomination :socket="socket" :id="'twenty'" :amount="20" />
                <Denomination :socket="socket" :id="'ten'" :amount="10" />
                <Denomination :socket="socket" :id="'five'" :amount="5" />
                <Denomination :socket="socket" :id="'two'" :amount="2" />
                <Denomination :socket="socket" :id="'one'" :amount="1" />
              </table>
            </td>
          </tr>
          <tr>
            <td>
              Total: {{ currencySymbol() }}{{ total() }}
            </td>
          </tr>
        </table>
      </div>
    </div>
  </div>
</template>

<script>
import Denomination from './denominations/Denomination.vue'

export default {
  components: {
    Denomination
  },
  props: [
    'socket'
  ],
  data() {
    return {
      showDenominations: false
    }
  },
  computed: {
    editingWorkshop() {
      return this.$store.getters.getEditingWorkshop
    },
    editingGame() {
      return this.$store.getters.getEditingGame
    }
  },
  methods: {
    setShowDenominations(val) {
      this.showDenominations = val
    },
    scope() {
      return this.editingWorkshop ? this.editingWorkshop : this.editingGame
    },
    currencySymbol() {
      return this.scope().currency.symbol
    },
    total() {
      let total = 0
      for (const denomination in this.editingWorkshop.denominations) {
        total += this.editingWorkshop.denominations[denomination] * denomination
      }
      const pounds = Math.floor(total / 100)
      let pence = total - pounds * 100
      if (pence < 10) {
        pence = '0' + pence
      }
      return pounds + ':' + pence
    },
    getWidth(n) {
      let sum = 0
      for (const denomination in this.denominations) {
        sum = sum + parseInt(this.denominations[denomination])
      }
      return (n / sum) * 100 + '%'
    },
    updateCurrency() {
      const workshop = this.editingWorkshop.workshopName
      const game = this.editingGame.gameName
      const currency = document.getElementById('currency').value
      this.socket.emit('updateCurrency', {workshopName: workshop, gameName: game, currency: currency })

    }
  }
}
</script>

<style scoped lang="scss">
  .denominations-table {
    width: 100%;

    td {
      vertical-align: middle !important;
    }
  }
</style>
